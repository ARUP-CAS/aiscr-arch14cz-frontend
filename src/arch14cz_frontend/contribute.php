<h2 class="first">Contribute</h2>
<p>Did we miss any radiocarbon dates? Do you want to contribute with your dates? We will be happy if you will help us to develop the Arch14CZ.</p>
<p>You can contribute in two ways:</p>
<ol>
    <li>Let us know about newly published radiocarbon dates or errors in the database. Send a message to <a href="mailto:vondrovsky@arup.cas.cz">vondrovsky@arup.cas.cz</a>. You can attach the PDF of the paper/book with published dates.</li>
    <li>Fill in information about new dates in this <a href="static/arch14CZ_import_sample_contribute.xlsx">XLSX import file</a>. The pre-filled examples will guide you during the process. Fill in as much information as possible, but not all entries are compulsory. Send the filled document to <a href="mailto:vondrovsky@arup.cas.cz">vondrovsky@arup.cas.cz</a></li>
</ol>
<p>Your suggestions and data will be processed during the next scheduled update of the database.</p>
